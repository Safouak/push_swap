char	*ft_strcat(char *s1, const char *s2)
{
	char		*ptr1;
	const char	*ptr2;

	ptr1 = s1;
	while (*ptr1)
		ptr1++;
	ptr2 = s2;
	while (*ptr2)
	{
		*ptr1 = *ptr2;
		ptr2++;
		ptr1++;
	}
	*ptr1 = '\0';
	return (s1);
}
